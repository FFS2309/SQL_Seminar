SELECT e.FIRST_NAME, e.LAST_NAME, e.PHONE_NUMBER, substr(e.FIRST_NAME, 1, 1) || substr(e.LAST_NAME, 1, 1) || substr(e.PHONE_NUMBER, length(e.PHONE_NUMBER) - 3, 4) FROM EMPLOYEES e;
-- 52.
SELECT e.EMPLOYEE_ID || rpad('*', ROUND(e.SALARY/500) ,'*') FROM EMPLOYEES e;
-- 53.
SELECT substr(e.LAST_NAME, 1, 1), COUNT(*) FROM EMPLOYEES e GROUP BY substr(e.LAST_NAME, 1, 1) ORDER BY substr(e.LAST_NAME, 1, 1);
-- 54.
SELECT FIRST_NAME, LAST_NAME,
       CASE WHEN e.SALARY < 4000 THEN
            'LOW'
        WHEN e.SALARY < 8000 THEN
            'MEDIUM'
        ELSE
            'HIGH'
        END
       FROM EMPLOYEES e;
