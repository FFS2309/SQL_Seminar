SELECT TO_CHAR(e.HIRE_DATE, 'YYYY'), COUNT(*) FROM EMPLOYEES e GROUP BY TO_CHAR(e.HIRE_DATE, 'YYYY') ORDER BY TO_CHAR(e.HIRE_DATE, 'YYYY');
